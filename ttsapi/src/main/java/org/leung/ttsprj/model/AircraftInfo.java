package org.leung.ttsprj.model;

public class AircraftInfo {

    private String flight;
    private String gs;
    private String lat;
    private String lon;

    public String getFlight() {
        return flight;
    }

    public String getGs() {
        return gs;
    }

    public String getLat() {
        return lat;
    }

    public String getLon() {
        return lon;
    }

}
